/**
 * Generates large mock lockfiles under test-packages/ for stress tests or demos.
 * Re-run after changing PACKAGE_COUNT or formats: node scripts/generate-test-package-lockfiles.mjs
 */

import fs from 'node:fs';
import path from 'node:path';

import { TEST_PACKAGES_DIR, integrityFor } from './test-packages-shared.mjs';

const OUT_DIR = TEST_PACKAGES_DIR;
const MIN_LINES = 5000;

/** Enough mock packages that each emitted file exceeds MIN_LINES. */
const PACKAGE_COUNT = 1020;

function buildPnpmLockfile() {
  const lines = [];
  lines.push("lockfileVersion: '9.0'", '');
  lines.push('settings:');
  lines.push('  autoInstallPeers: true');
  lines.push('');
  lines.push('importers:');
  lines.push('');
  lines.push('  .:');
  lines.push('    dependencies:');
  for (let i = 0; i < PACKAGE_COUNT; i++) {
    const name = `mock-dep-${i}`;
    const ver = `1.${i}.0`;
    lines.push(`      ${name}:`);
    lines.push(`        specifier: ${ver}`);
    lines.push(`        version: ${ver}`);
  }
  lines.push('');
  lines.push('packages:');
  lines.push('');
  for (let i = 0; i < PACKAGE_COUNT; i++) {
    const name = `mock-dep-${i}`;
    const ver = `1.${i}.0`;
    const key = `${name}@${ver}`;
    lines.push(`  ${key}:`);
    lines.push(`    resolution: {integrity: ${integrityFor(i)}}`);
    lines.push(`    engines: {node: '>=18'}`);
  }
  lines.push('');
  lines.push('snapshots:');
  lines.push('');
  for (let i = 0; i < PACKAGE_COUNT; i++) {
    const name = `mock-dep-${i}`;
    const ver = `1.${i}.0`;
    const key = `${name}@${ver}`;
    lines.push(`  ${key}: {}`);
  }
  lines.push('');
  return lines.join('\n');
}

function buildNpmPackageLock() {
  const packages = {};
  const rootDeps = {};
  packages[''] = {
    name: 'mock-test-root',
    version: '1.0.0',
    license: 'MIT',
    dependencies: rootDeps,
  };
  for (let i = 0; i < PACKAGE_COUNT; i++) {
    const name = `mock-dep-${i}`;
    const ver = `1.${i}.0`;
    rootDeps[name] = ver;
    packages[`node_modules/${name}`] = {
      version: ver,
      resolved: `https://registry.npmjs.org/${name}/-/${name}-${ver}.tgz`,
      integrity: integrityFor(i),
      license: 'MIT',
      engines: { node: '>=18' },
    };
  }
  const doc = {
    name: 'mock-test-root',
    version: '1.0.0',
    lockfileVersion: 3,
    requires: true,
    packages,
  };
  return `${JSON.stringify(doc, null, 2)}\n`;
}

function buildYarnLock() {
  const lines = [];
  lines.push('# THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY.');
  lines.push('# yarn lockfile v1');
  lines.push('');
  lines.push('');
  for (let i = 0; i < PACKAGE_COUNT; i++) {
    const name = `mock-dep-${i}`;
    const ver = `1.${i}.0`;
    lines.push(`${name}@${ver}:`);
    lines.push(`  version "${ver}"`);
    lines.push(`  resolved "https://registry.npmjs.org/${name}/-/${name}-${ver}.tgz"`);
    lines.push(`  integrity ${integrityFor(i)}`);
    lines.push('');
  }
  return lines.join('\n');
}

function lineCount(text) {
  let n = 0;
  for (let k = 0; k < text.length; k++) if (text[k] === '\n') n++;
  if (text.length && !text.endsWith('\n')) n++;
  return n;
}

fs.mkdirSync(OUT_DIR, { recursive: true });

const pnpm = buildPnpmLockfile();
const npm = buildNpmPackageLock();
const yarn = buildYarnLock();

fs.writeFileSync(path.join(OUT_DIR, 'pnpm-lock.yaml'), pnpm, 'utf8');
fs.writeFileSync(path.join(OUT_DIR, 'package-lock.json'), npm, 'utf8');
fs.writeFileSync(path.join(OUT_DIR, 'yarn.lock'), yarn, 'utf8');

for (const [label, text] of [
  ['pnpm-lock.yaml', pnpm],
  ['package-lock.json', npm],
  ['yarn.lock', yarn],
]) {
  const lc = lineCount(text);
  if (lc < MIN_LINES) {
    console.error(`${label}: ${lc} lines (need >= ${MIN_LINES}). Increase PACKAGE_COUNT.`);
    process.exit(1);
  }
  console.log(`${label}: ${lc} lines`);
}

console.log(`Wrote ${OUT_DIR}`);
